    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int haid = Integer.parseInt(adminIDText.getText());
        String haname = adminNameText.getText();
        String haemail = adminEmailText.getText();
        String Password = new String(adminPasswordText.getPassword());

        String namePattern = "[a-zA-Z_ ]+";

        String adminNamePattern = "[a-zA-Z_ ]+";
        String adminEmailPattern = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        String adminPasswordPattern = "^(?=.*[0-9])"
                + "(?=.*[a-z])(?=.*[A-Z])"
                + "(?=.*[@#$%^&+=])"
                + "(?=\\S+$).{8,20}$";

        if (haname.isEmpty() || haemail.isEmpty() || Password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill All The Fields", "Warning",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!haname.matches(namePattern)) {
            JOptionPane.showMessageDialog(this, "Enter correct details", "Warning",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!haemail.matches(adminEmailPattern)) {
            JOptionPane.showMessageDialog(this, "Enter a Valid Email", "Warning",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!Password.matches(adminPasswordPattern)) {
            JOptionPane.showMessageDialog(this, "Enter Valid Passowrd", "Warning",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            //when all conditions are false
            for (Hospitals hospital : hosdetail) {
                if (HospitalAdminLogin.hadminID == hospital.idP) {
                    hospital.idP = haid;
                    hospital.nameP = haname;
                    hospital.emailP = haemail;
                    hospital.adminPassword = Password;
                    hospital.city = cityTextField.getText();
                    hospital.streetName = streetTextField.getText();
                    hospital.house = aptTextField.getText();
                    
                    JOptionPane.showMessageDialog(this, "Data Updated Sucessfully", "Warning",
                    JOptionPane.ERROR_MESSAGE);
                }
            }
            
        }

    }//GEN-LAST:event_jButton1ActionPerformed

